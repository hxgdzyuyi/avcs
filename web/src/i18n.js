export function normalizeLocale(value) {
  const locale = String(value || "").trim().toLowerCase();
  return SUPPORTED_LOCALE_VALUES.has(locale) ? locale : DEFAULT_LOCALE;
}

export function createTranslator(locale) {
  const normalizedLocale = normalizeLocale(locale);
  return (key, params = {}, fallback = key) =>
    translate(normalizedLocale, key, params, fallback);
}

export function translate(locale, key, params = {}, fallback = key) {
  const normalizedLocale = normalizeLocale(locale);
  const template =
    TRANSLATIONS[normalizedLocale]?.[key] ??
    TRANSLATIONS[DEFAULT_LOCALE]?.[key] ??
    fallback ??
    key;

  return interpolate(template, params);
}

function interpolate(template, params) {
  return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => {
    if (!Object.prototype.hasOwnProperty.call(params || {}, name)) return match;
    const value = params[name];
    return value === null || value === undefined ? "" : String(value);
  });
}
